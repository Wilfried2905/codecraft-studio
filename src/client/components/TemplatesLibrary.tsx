import { useState } from 'react'
import { X, LayoutGrid } from 'lucide-react'
import { TEMPLATES, CATEGORIES, Template } from '../constants/templates'

interface TemplatesLibraryProps {
  isOpen: boolean
  onClose: () => void
  onSelectTemplate: (template: Template) => void
}

export default function TemplatesLibrary({ isOpen, onClose, onSelectTemplate }: TemplatesLibraryProps) {
  const [activeCategory, setActiveCategory] = useState('all')
  
  const filteredTemplates = activeCategory === 'all'
    ? TEMPLATES
    : TEMPLATES.filter(t => t.category === activeCategory)
  
  if (!isOpen) return null
  
  return (
    <div className="modal-overlay" onClick={onClose}>
      <div className="modal-content max-w-6xl" onClick={e => e.stopPropagation()}>
        {/* Header */}
        <div className="p-6 border-b border-slate-700 flex justify-between items-center">
          <h2 className="text-2xl font-bold text-white flex items-center gap-2">
            <LayoutGrid className="w-6 h-6 text-primary-400" />
            Templates Library
            <span className="text-slate-500 text-lg">({filteredTemplates.length})</span>
          </h2>
          <button
            onClick={onClose}
            className="p-2 text-slate-400 hover:text-white hover:bg-slate-800 rounded-lg transition-colors"
          >
            <X className="w-6 h-6" />
          </button>
        </div>
        
        {/* Categories */}
        <div className="px-6 py-4 flex gap-2 overflow-x-auto border-b border-slate-800">
          {CATEGORIES.map(category => {
            const Icon = getIconComponent(category.icon)
            const isActive = activeCategory === category.id
            
            return (
              <button
                key={category.id}
                onClick={() => setActiveCategory(category.id)}
                className={`px-4 py-2 rounded-lg text-sm font-medium whitespace-nowrap transition-all flex items-center gap-2 ${
                  isActive
                    ? 'bg-primary-500 text-white shadow-lg'
                    : 'bg-slate-800 text-slate-400 hover:bg-slate-700'
                }`}
              >
                <Icon className="w-4 h-4" />
                {category.name}
              </button>
            )
          })}
        </div>
        
        {/* Templates Grid */}
        <div className="p-6 overflow-y-auto max-h-[60vh] grid md:grid-cols-2 lg:grid-cols-3 gap-4">
          {filteredTemplates.map(template => {
            const Icon = getIconComponent(template.icon)
            
            return (
              <div
                key={template.id}
                onClick={() => {
                  onSelectTemplate(template)
                  onClose()
                }}
                className="card cursor-pointer hover:scale-105 transition-all group"
              >
                <div className="flex gap-3 mb-3">
                  <div className="p-3 bg-primary-500/10 rounded-lg group-hover:bg-primary-500/20 transition-colors">
                    <Icon className="w-6 h-6 text-primary-400" />
                  </div>
                  <div className="flex-1 min-w-0">
                    <h3 className="font-semibold text-white mb-1 truncate">{template.name}</h3>
                    <p className="text-sm text-slate-400 line-clamp-2">{template.description}</p>
                  </div>
                </div>
                <div className="flex items-center justify-between mt-4 pt-3 border-t border-slate-700">
                  <span className="text-xs text-slate-500 uppercase tracking-wide">{template.category}</span>
                  <span className="text-xs text-primary-400 font-medium opacity-0 group-hover:opacity-100 transition-opacity">
                    Utiliser →
                  </span>
                </div>
              </div>
            )
          })}
        </div>
        
        {/* Empty State */}
        {filteredTemplates.length === 0 && (
          <div className="p-12 text-center text-slate-500">
            <LayoutGrid className="w-16 h-16 mx-auto mb-4 opacity-50" />
            <p className="text-lg">Aucun template trouvé dans cette catégorie</p>
          </div>
        )}
      </div>
    </div>
  )
}

